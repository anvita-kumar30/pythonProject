# Arithmetic Operators
print("Arithmetic  Operators")
print("5 + 6 is ", 5+6)
print("5 - 6 is ", 5-6)
print("5 * 6 is ", 5*6)
print("5 / 6 is ", 5/6)
print("5 ** 3 is ", 5**3)  #power
print("5 % 5 is ", 5%5)
print("15 // 6 is ", 15//6)  #floor division operator(double divide)
print()

# Assignment Operators
print("Assignment Operators")
x = 5
print(x)
x += 7   #x = x+7
print(x)
x %= 7   #x = x%7
print(x)
print()

# Comparison Operators
print("Comparison Operators")
i = 5
print(i==8)
print(i==5)
print()


# Logical Operators
print("Logical Operators")
a = True
b = False
print(a and a)
print(a and b)
print(a or a)
print(a or b)
print()

# Identity Operators
print("Identity Operators")
c = 2
d = 4
print(c is not d)
print(c is d)
print()

# Membership Operators
print("Membership Operators")
list = [3, 3, 2, 2, 39, 33, 35, 32]
print(324 not in list)
print(32 not in list)
print(32 in list)
print()

